        <h2 class="featurette-heading">Metode valg. <span class="sidenavn">Gangster-test.</span></h2>
        <p class="lead">Gangster-test er en kvalitativ undersøgelses form. Vi valgte denne testform, da den danner et rigtig godt udgangspunkt at arbejde ud fra, da det er en test af websitets informationsarkitektur, den skal derfor sikre at de vigtigste hjælpemidler er på plads den siden, samt at siden overhoder de mest almindelige koventioner. Testen er ikke voldsom dybdegående men alligevel danner den et overblik som er vigtigt til videre udvikling, samt fortæller om man har ramt rigtig eller forkert når man snakker brugervenlighed.</p>
        <p class="lead">Denne test fungere på en måde så der er 6 spørgsmål som brugeren skal fokusere på, under hvert punkt skal brugeren så give points fra 0-3</p>
        <ul>
          <li><span class="tyk">0 point:</span> Denne information fremgår slet ikke af undersiden</li>
          <li><span class="tyk">1 point:</span> Denne information fremgår kun delvis af undersiden / svær at finde</li>
          <li><span class="tyk">2 point:</span> Denne information kunne fremgå tydeligere på undersiden</li>
          <li><span class="tyk">3 point:</span> Denne information fremgår tydeligt på undersiden. </li>
        </ul>
        <p class="lead">Dette ender ud i en mulig total på 0-18 point. hvor 0 er udtryk for, at navigationen er dårligt understøttet, og 18 er udtryk for, at navigationen er meget tydeligt og godt understøttet.</p>
        <p class="lead">Testen starter med at spørge om hvilket website man er på, dette er meget væsentligt da det er vigtigt at kunne se hvilken side man befinder sig på, uden at skulle kigge på browserens søgefelt.</p>
        <p class="lead">Andet spørgsmål ligger på om der er en ordentlig navigations menu på siden. Dette er vigtigt for brugervenligheden på siden, da det hjælper ens brugere med at finde frem til de ting som de søger.</p>
        <p class="lead">Derefter bliver der spurgt til underside, dette felt er måske ikke lige så vigtigt for alle sider, men stadig en god ting at kunne se hvor man egentlig befinder sig, så man kan finde frem til de ting eller information igen.</p>
        <p class="lead">Så bliver der spurgt til hvilke muligheder man har på siden, om der er undermenuer, links downloads osv.</p>
