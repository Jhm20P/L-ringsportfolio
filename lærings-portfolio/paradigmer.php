        <h2 class="featurette-heading">Humanistiske paradigme. <span class="sidenavn">IMK-modellen</span></h2>
        <p class="lead">IMK-modellen er en klassisk kommunikationsmodel her i danmark angående kommunikationsfag. Betegnelsen IMK står for International Markeds Kommunikation. </p>
        <p class="lead">Modellens elementer</p>
        <ul>
          <li>Afsender
            <ul>
              <li>Er den person som har sørget for kommunikationen samt den der har besluttet hvordan og hvornår det skal finde sted. Den teksteksterne afsender kan være en reklameannonce, Det vil sige at i dette tilfælde så er det reklamebureauet der har sat det op. Den tekstinterne afsender er vedkommende der explicit markeret i teksten. Den primære afsender vil være en virksomhed f.eks.  som er synlig i en annonce, hvor den sekundære afsender kunne f.eks. være en form for specialist inden for det produkt der er i annoncen.
              </li>
            </ul>
          </li>
          <li>Modtager
            <ul>
              <li>En modtager er den person som tager imod den kommunikation/budskab som afsenderen kommer med. Ligesom ved afsender, så skelner man også mellem tekstekstern og tekstintern. Den tekstinterne modtager vil være markeret i teksten fra afsenderen, hvor den teksteksterne ikke er tilstede i teksten selv, men derimod vedkommende der læser teksten, hvor det stadig træder i kraft selvom at modtageren er uden for målgruppen.</li>
            </ul>
          </li>
          <li>Teksten
            <ul>
              <li>Teksten er den del der skal forstås som den kommunikative sammenhæng mellem afsender og modtager i forhold til afsenderens produktion og modtagerens reception</li>
            </ul>
          </li>
          <li>Referenten
            <ul>
              <li>Referenten er alt det som der bliver refereret til i teksten. Det kan f.eks. være et produkt, dog vil det typisk være den effekt, som et produkt giver, eller det kan også være et brand som står bag et produkt.</li>
            </ul>
          </li>
          <li>Koden
            <ul>
              <li>Koden er det der kan forstås som det sprog modtageren forstår. Det vil også betyde at ens valg af medier samt gerne vil have en indflydelse på den kode. Man skal selvfølgelig huske at nogle ting betyder noget forskelligt afhængig af kultur</li>
            </ul>
          </li>
          <li>Kontekst/kultur
            <ul>
              <li>Er den konkrete situation, som indebærer kommunikationssituationen. Kan beskrives med andre ord som, “Hvem”, “Hvad”, “Hvor” og “Hvornår”. Men der vil så stadig den kultur, som påvirker situationen.  Kultur er noget der kan ses på flere forskellige niveauer, som er lige fra det nære til det nationale samt internationale.Din kontekst samt den kultur du arbejder i, vil være det der afgøre hvilket medie og retoriske strategier/metoder du vælger at bruge. Man skal også have det i bagtanken, at det der er positivt i en kultur kan være negativ i en anden.</li>
            </ul>
          </li>
          <li>Medier
            <ul>
              <li>Medie er der hvor kommunikationssituationen befinder sig.  Et medie kan være printede medier som aviser, elektroniske medier kan være Facebook eller Twitter eller det kan også være mennesket selv der fungere som et medie. Det er vigtigt at du gøre dig nogle overvejelser omkring anvendelse samt situation, da medier har flere forskellige forhold af indholdsmæssig samt praktisk karakter.</li>
            </ul>
          </li>
          <li>Genre
            <ul>
              <li>I IMK-modellen kan der ses et budskab for hvordan kultur/kontekst og medier samt retoriske strategier kan arbejde sammen.
                <ul>
                  <li>Trækstrukturer forstås som de sproglige træk som der hver især er med til at opfylde formålet for det kommunikative. Det vil så også sige at tekster som har det samme formål, typisk også har nogle træk tilfælles.</li>
                  <li>Retoriske virkemidler kan være anvendelse af fagbegreber, slogans, indforstået ordspil eller intertekstuelle referencer samt logo og farver.</li>
                </ul>
              </li>
            </ul>
          </li>
        </ul>
