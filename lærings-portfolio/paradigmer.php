        <h2 class="featurette-heading">Samfundsvidenskabelige paradigme.</h2>
        <p class="lead">Det samfundsvidenskabelige paradigme har fokus på afsenderen og dens placering i omverdenen. Det er i dette paradigme at afsenderen ses som den styrende i forhold til kommunikationsprocessen, hvor paradigmet er styres tildeles af en forståelse for kommunikation som transmission. Hvis man kigger på den traditionelle del ved samfundsvidenskabelige paradigme, vil man kigge på fagområderne samfundsvidenskab, sociologi, politik, økonomi, jura osv. De klassiske retninger inden for det videnskabsteoretiske i dette paradigme, vil man kigge på systemteori samt funktionalisme, der egentligt oprindeligt stammer fra en biologisk forklaring sammenhæng. Det skal dog siges at inden for de seneste år, har det samfundsvidenskabelige paradigme taget flere metoder samt teori fra det humanistiske paradigme. Når man skal undersøge noget i det samfundsvidenskabelige paradigme, vil man typisk bruge kvantitative metode for at få en mere generel forståelse og data på det man undersøger.</p>
