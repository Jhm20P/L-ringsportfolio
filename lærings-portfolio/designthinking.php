      <div class="col-md-7">
        <h2 class="featurette-heading">Design thinking. <span class="sidenavn">Indledning.</span></h2>
        <ul>
          <li>Empathise
            <ul>
              <li>Er hvor man søger/researcher alt det man kan omkring det område, emne eller ting man skal undersøge. Så hvis du har om et produkt du gerne vil lave inden for et bestemt område, vil du researche alt hvad du kan inden for det område, samt se hvordan andre har gjort det, for at give inspiration. </li>
            </ul>
          </li>
          <li>Define
            <ul>
              <li>Define er hvor du beskriver det problem du har ved dit område, eller det problem du gerne vil fokusere på. Problemet vil du helst gerne formulere som en problemstilling.</li>
            </ul>
          </li>
          <li>Ideate
            <ul>
              <li>Her vil man brainstorme, altså komme med ideér til hvad man kan inden for det problem man har defineret i sin define fase. Det man kan gøre efter at man har lavet en brainstorm er et mindmap, for at kunne kategorisere sine ideér samt skabe et overblik. Det man også kan vælge at gøre efter at man har lavet sit mindmap er et problemtræ, hvorefter problemtræet, laver man en afgrænsning af problemtræet, for at komme mere ind på hvad man gerne vil fokusere på.</li>
            </ul>
          </li>
          <li>Prototype
            <ul>
              <li>inden man går i gang med selve prototypen, kan man lave et pv-skema hvor man skriver inden hvilke krav sin idé skal overholde, samt man sætter en værdig på de krav man er kommet med, for at kunne se hvilke krav der er vigtigst. Men når man så har lavet det her pv-skema kan man gå i gang med at lave sin prototype.</li>
            </ul>
          </li>
          <li>Test
            <ul>
              <li>Her vil man teste sin prototype på folk, helst gerne på en inden for sin modtagergruppe. Det man vil få ud en test, er at man kan se om der er noget der skal rettes på, hvad du kan gøre bedre ved dit produkt samt du kan se hvilke krav den overholdte i forhold til dit pv-skema.</li>
            </ul>
          </li>
          <li>Implementering
            <ul>
              <li>Er hvor man implementere det ind som man har fået feedback på fra din testfase.</li>
            </ul>
          </li>
        </ul>
