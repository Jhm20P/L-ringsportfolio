        <p class="lead">Styrkelse af lokalsamfundet.</p>
        <ul>
          <li>Problem
            <ul>
              <li>Affolkning af lokalsamfund og øget metropolisering.</li>
              <li>Øget metropolisering medfører større forurening i storbyerne, som bl.a. medfører dårligere helbred (fysisk og psykisk.</li>
            </ul>
          </li>
          <li>Brugere
            <ul>
              <li>Beboere (og potentielle beboere) i lokalsamfund.</li>
              <li>Det er et problem med den øgede tilgang til store byer. Huse og lejligheder bliver for dyre at bo i for mange mennesker. Ved at gøre det mere attraktivt at bo i lokalsamfund, kan vi mindske tilgangen til de større byer</li>
              <li>Gør det muligt at bevare lokalsamfund.</li>
              <li>Giver borgere i lokalsamfundene flere muligheder.</li>
              <li>Og dermed mindske det antal borgere, som udsættes for den øgede forurening.</li>
            </ul>
          </li>
          <li>Mulige spørgsmål til potentielle brugere af app’en
            <ul>
              <li>Hvorfor bor du her?</li>
              <li>Hvorfor er du flyttet dertil</li>
              <li>Hvorfor bor du der stadig?</li>
              <li>Hvilken tilknytning har du til det her sted.</li>
              <li>Hvilke uddannelse har du?</li>
            </ul>
          </li>
          <li>Fordele ved at bo på landet
            <ul>
              <li>Lavere huspriser per m2.</li>
              <li>Mindre larm fra trafik.</li>
              <li>Mere plads, mere frihed til at udfolde sig.</li>
              <li>Bedre muligheder for at have kæledyr.</li>
              <li>Mindre stress over at skulle tjene tilstrækkelig penge (pga. lavere huspriser)</li>
              <li>Tættere forhold til naboerne.</li>
              <li>Bedre sikkerhed for legende børn (udenfor)</li>
              <li>Pandemier er ikke så farlige her pga. større afstand mellem mennesker.</li>
            </ul>
          </li>
          <li>Ulemper ved at bo på landet
            <ul>
              <li>Der er langt til butikker - specielt specialbutikker, samt natteliv.</li>
              <li>Større CO2-udledning pga. mere transport i egen bil.</li>
              <li>Tager ekstra tid at komme på arbejde/uddannelse/skole.</li>
              <li>Dårlige offentlige transportmuligheder.</li>
              <li>Kan være svært at sælge sin bolig.</li>
