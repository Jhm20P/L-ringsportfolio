        <h2 class="featurette-heading">Lokalsamfund. <span class="text-muted">Checkmate.</span></h2>
        <p class="lead">Hej Helle. Vi er en gruppe multimediedesigner-studerende på Erhversakademi Dania i Skive, som er i gang med at undersøge problemet omkring affolkning af landområder. I den forbindelse tænker vi, at du har en stor viden, som vi måske kan drage nytte af. Vi håber du vil svare på nedenstående spørgsmål.</p>
        <ul>
          <li>Er affolkning af Skive Kommune et problem?
            <ul>
              <li>Ja, som for alle andre kommuner.</li>
              <li>Politikere påvirker os som befolkning.</li>
              <li>Unge flytter væk.</li>
              <li>Bevarelse af skoler og daginstitutioner påvirkes af fraflytningen.</li>
              <li>Befolkningens alders-sammensætning ændres ved at ugen flytter fra komunen. Man bliver nødt bliver nødt til at tilpasse serviceydelser.</li>
            </ul>
          </li>
          <li>Hvad gør Skive Kommune for at tiltrække borgere fra andre kommuner?
            <ul>
              <li>Bosætnings-strategi.</li>
              <li>Ingen kampagner.</li>
              <li>Målrettet indsat (Klub 78xx), unge har en lokal stolthed, men også modvilje mod at flytte tilbage. Det prøver vi at spille på ved at gøre dem opmærksom på gode arbejdspladser og Rent Liv f.eks.</li>
              <li>Skive Kommune deltager i karrieremessser for alle studieretninger.</li>
              <li>Indsats for at midlertidigere borgere (f.eks.uddannelsessøgende) bliver engageret i sociale netværk, og dermed får en tilknytning til byen ud over deres uddannelse.</li>
            </ul>
          </li>
          <li>Eller har man mest fokus på blot at sørge for at borgerne ikke flytter fra Skive kommune?
            <ul>
              <li>Fællesskabsfølelse.</li>
              <li>Stolthed som f.eks. rent liv.</li>
              <li>Stolthed omkring plakat med hilsen fra borgmesteren.</li>
              <li>Åbredsfesten i aug/sep på elværksgrunden med debatter, musik, havefest, 200 personer sidste år.</li>
              <li>Gøre skiveegnen til et godt sted at leve.</li>
              <li>Fremtidsbutikken i Thise.</li>
              <li>Vejliedning ifm. modtagelse af nye borgere.</li>
            </ul>
          </li>
          <li>Hvilke yderligere tiltag tænker du der evt. kunne være nødvendige, for at sikre at borgere ikke fraflytter kommunen?
            <ul>
              <li>At borgere sætter netværk i søen (det er bedre at initiativerne groer nedefra end at kommunen iværksætter dem).</li>
              <li>Ideer vil blive hjulpet frem af kommunen. Det vil være fænomenalt hvis lokale borgere tager initiativer.</li>
              <li>Internationale borgere (højt kvalificerede) er lokalt forankrede indtil deres børn skal i skole. De vil så flytte til en by med en international skole, så børnene er klar til at flytte tilbage til udgangslandet.</li>
              <li>Omfartsvejen ved Haderup er et skridt i den rigtige retning.</li>
            </ul>
          </li>
          <li>Hvad synes du byen Skive mangler for at tiltrække flere borgere?
            <ul>
              <li>Skive Kommune er “langt væk” og bliver nogle gange (fejlagtigt assoiret med udkants-dk)</li>
            </ul>
          </li>
          <li>Har du andre kommentarer til emnet “affolkning af landområder”, som du synes vi bør have.
            <ul>
              <li>Der skal sættes ind både lokalt, regionalt og nationalt.</li>
              <li>Det er normalt at fraflytte.</li>
              <li>Gør kommunen nok?</li>
              <li>Det er en kulturændring, der skal til for at sætte Skive og lignende kommuner i et bedre lys.</li>
              <li>Lokale uddannelser er et must. Det er en national indsats.</li>
              <li>Mange drømmer om et liv på landet, men realiserer det ikke. Måske kan den digitalisering, som vi oplever lige nu pga. Corona, få flere til at gøre noget ved drømmen.</li>
            </ul>
          </li>
        </ul>
