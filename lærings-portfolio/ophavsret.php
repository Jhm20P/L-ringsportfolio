          </li>
          <li>Redegør for de seks licensering former der findes under Creative Commons
            <ul>
              <li>Navngivelse(by)
                <ul>
                  <li>Ved denne licens tillader man at andre kan distribuere dit værk, som ændre på det samt tilpasse det, og bruge det kommerciel, så længe at de kreditere dig(rettighedshaverne)</li>
                </ul>
              </li>
              <li>Del på samme vilkår(Share-alike)
                <ul>
                  <li>Den her licens tillader at andre kan bearbejde, hvilket medfølger at de kan tilpasse samt ændre dit værk samt bruge det kommerciel sigte, så længe at de kreditere dig og licensere det nye værk på samme vilkår.</li>
                </ul>
              </li>
              <li>Navngivelse - ingen bearbejdelser
                <ul>
                  <li>Den her licens tillader at man kan sprede værker til offentligheden kommerciel såvel som ikke-kommerciel, på baggrund af at værket distribueres uændret med kreditering til rettighedshaveren</li>
                </ul>
              </li>
              <li>Navngivelse-ikke-kommerciel
                <ul>
                  <li>Den her licens tillader at andre kan bearbejde, hvilket medfølger at de kan tilpasse samt ændre dit værk, dog med ikke kommerciel sigte. Der skal stadig krediteres til dig ved de nye værke samt at de nye værker må ikke bruges kommerciel. Dog er det ikke nødvendigt at der bliver licenseret under samme vilkår.</li>
                </ul>
              </li>
              <li>Navngivelse-ikke-kommerciel-Del på samme vilkår
                <ul>
                  <li>Den her licens tillader at andre kan bearbejde, samt tilpasse og ændre dit værk sålænge at de ikke bruger det kommercielt, på betingelse af at de kreditere dig samt licensere det nye værk på lignende vilkår. Andre kan dog downloade samt sprede dit værk til offentligheden ligesom ved den sidste licens. Men de må dog gerne oversætte, ændre forfatte nye værker på baggrund af dit værk. Alle nye værker der er blevet lavet på baggrund af dit, skal licenseres på lige vis.</li>
                </ul>
              </li>
              <li>Navngivelse-ikke-kommerciel- ingen bearbejdelser
