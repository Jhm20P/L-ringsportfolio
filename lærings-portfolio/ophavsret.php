        <p class="lead">Ophavsretten er det der sørger for at folk ikke kan tage/stjæle noget af det du har lavet, så længe det du har lavet er originalt, altså at du selv har lavet det fra bunden. Det kan også være at du har taget noget fra nogle andre i forhold til noget musik, men det meste af beaten i det musik du udgiver er dit, så har du stadig ophavsret på det.</p>
        <p class="lead"> I Danmark har vi ophavsrets paragrafferne §1 som er beskyttelse af værker, §66 er om lyd, §67 er om billeder og til sidst er der §70 som er fotografier for interesse. Der er selvfølgelig også andre ting som tekster, skulpturer, malerier, covers samt lignende værker der er beskyttet. Ens kreationer som man selv har skabt har automatisk ophavsret, selvom man ikke gøre folk klar over det ved at f.eks. bruge et copyright tegn på ens hjemmeside.</p>
        <p class="lead">Skal man altid spørge om lov før man bruger en andens værk?</p>
        <ul>
          <li>Ophavsretten også kendt som det engelske ord “Copyright” og det har symbolet: ©.
      Det ophavsretten betyder, er at man ikke må tage værker, såsom film, musik, billeder og
      malerier osv. fra dem der har skabt det, med mindre de giver én lov til at bruge det.
      Denne  rettighed gælder generelt for 70 år eller til at skaberen er død men kun 50 år når
      det gælder musik. Det vil sige at rock’n roll fra 1950’erne kan blive brugt frit i dag, dog
      ved billeder kan man bruge alle billeder der blev taget før 1970.</li>
        </ul>
        <p class="lead">Kom specielt ind på ophavsretten på sociale medie, herunder:</p>
        <ul>
          <li>Brugen af værker fra sociale medier
            <ul>
              <li>Når det kommer til sociale medier gælder de samme regler som for alle andre punkter i ophavsretsloven.  Bruger du Facebook eller Twitter skal man være opmærksom på at man skal overholde reglerne for upload samt linking. F.eks. må du ikke lægge billeder eller musik op på de sociale medier som andre har rettighederne på, altså ophavsretten på.  Du skal også overholde downloading og streaming af materiale som du bruger, men bliver delt af andre, det skal dog siges at det er ulovligt at downloade eller streame materiale som folk har delt ulovligt. Du skal dog være opmærksom på at når du opretter en Facebook, så giver du Facebook en licens til at bruge dine billeder og videoer.</li>
            </ul>
          </li>
