        <ul>
          <li> Beskriv hvad Creative Commons er
            <ul>
              <li>Creative commons er en organisation der viser sig at være nyttig over for Musikere, kunstnere, forfattere samt andre rettighedshavere. Dette gør at de sikkert, kan dele deres ting med andre.  Med creative commons kan rettighedshaverne gøre sådan at andre kan kopier deres værker/produkter som det lyster og forære dem væk, så længe at man kreditere rettighedshaverne samt at man ikke bruger deres værker til kommercielt brug.</li>
