        <h2 class="featurette-heading">Metoder.</h2>
        <p class="lead">Hvornår kan man med fordel bruge de forskellige metoder? Kom med eksempler.</p>
        <ul>
          <li>
            Man kan bruge kvantitativ metode til telefoninterviews, hvor det er intervieweren der fortæller samt udfylder skemaet. De gør også sådan at intervieweren kan forklare og hjælpe svar personen igennem spørgsmålet, dette mindsker chancen for misforståelser/usikkerhed(Systime, samfundsfaglige metoder, kvantitativ metode)
          </li>
          <li>
            I interview kan vi ændre vores spørgsmål alt afhængig af hvad svarpersonen svare og hvad svarpersonen tilføjer af pointer. Dette kan resulterede i at vi enten bliver nødt til at omformulere et spørgsmål, eller tilføje et underbyggende spørgsmål til det svar man får på baggrund af det forrige spørgsmål.(Systime, samfundsfaglige metoder, kvantitativ metode)
          </li>
