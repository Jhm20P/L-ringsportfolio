        <h2 class="featurette-heading">Observationsstudier.</h2>
        <p class="lead">Observationsstudier kan bruges når man laver en undersøgelse af en gruppe der initiere i en dialog/diskussion. Dem der opsamler dataene kan gøre det ved at optage det i video, lyd eller skrive det som deltagerne siger ned.  Der skal dog spørges om tilladelse til at optage video eller lyd. Når man spørger om tilladelse kan det påvirke hele dialogen som deltagerne initiere i. Dette kan så medfølge at man ikke helt får de svar man gerne vil have fra deltagerne.</p>
        <p class="lead">Man kan også selv være deltagende i den her observationsstudie, hvor man deltager aktivt i dialogen/diskussionen Når man anvender den her metode er det vigtigt at man finder personer der befinder sig i samme målgruppe eller samme segment på gallup kompasset. Men man skal samtidig have en ide om ideal modtager man går efter for at kunne perspektivere hvor tæt på ens målgruppe er på den her ideelle modtager.</p>
        <p class="lead"><cite>-Hanne, Christine, Alberte og Jonas, Moodle/<a class="google" href="https://docs.google.com/document/d/1kkICkVV3WivtOTOLgzQf-pGnR7_2ZyFWxYfqgvaT_s0/edit?usp=sharing" target="_blank">Google docs</a>, <time datetime="2020-09-22">22 sep, 2020</time></cite></p>
