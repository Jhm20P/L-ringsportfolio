            </ul>
          </li>
          <li>Vejen til kunderne
            <ul>
              <li>Er hvilke/hvilken måder/måde virksomheden kan nå ud til kunden/forbrugeren på. Det kan være sociale medier, reklame, radiospot, merchandise eller hjemmeside osv.</li>
            </ul>
          </li>
          <li>Omkostninger
            <ul>
