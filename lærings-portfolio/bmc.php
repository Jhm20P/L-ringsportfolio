          <li>Partnere
            <ul>
              <li>Er hvor man kigger efter hvilke partnere/sponsorere som den virksomhed man skal analysere har.</li>
            </ul>
          </li>
          <li>Aktiviteter
            <ul>
              <li>Er alt det hvor virksomheden gør for at forbedre sit image samt for at komme ud til forbrugeren. Det kan f.ek.s være en app, arrangementer, lancering af et nyt produkt, markedsføring eller en form for en udstilling.</li>
            </ul>
          </li>
          <li>Værdifaktorer
            <ul>
              <li>Er hvor man kigger hvilke gode ting som virksomheden har, som tager perspektiv fra kundens/brugerens side(synsvinkel.</li>
            </ul>
          </li>
          <li>Kunderelationer
            <ul>
              <li>Er hvor man kigger på hvilke relationer virksomheden har med kunden/forbrugeren. Det kan være en online butik(E-handel) samt fysiske butikker selvfølgelig. Det kan også være et arrangement som har noget med det produkt som virksomheden producere, hvor kunden er interesseret i arrangementet, ikke kunne på baggrund af det produkt som virksomheden har.</li>
            </ul>
          </li>
          <li>Kunderne
            <ul>
              <li>Kunderne er hvor man kigger efter kunder eller potentielle kunder, og det der ens målgruppe kommer ind i billedet. Der kan man bruge gallup kompasset eller minerva modellen.</li>
            </ul>
