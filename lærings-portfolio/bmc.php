        <p class="lead">Man kan bruge BMC til at forstå en virksomhed eller en forretning. Du kan sammenkoble BMC med Gallup kompasset, minerva modellen og en swot-analyse. Den kan hjælpe dig til at komme med nye idéer eller andre perspektiver til hvordan man kan forbedre en virksomhed eller en forretning.</p>
